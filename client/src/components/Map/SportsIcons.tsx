/**
 * Generate blob URL for SVG icon
 * Used for Mapbox image layer
 */
export const getSportIconDataURL = (sport: string): string => {
  const svg = SportIcons[sport as keyof typeof SportIcons] || SportIcons.default;
  const blob = new Blob([svg], { type: 'image/svg+xml' });
  return URL.createObjectURL(blob);
};

/**
 * Get sport icon SVG string
 */
export const getSportIcon = (sport: string): string => {
  return SportIcons[sport as keyof typeof SportIcons] || SportIcons.default;
};
